#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Fn,
    Print,
    Let,
    Pass,
    Return,
    Break,
    Continue,
    If,
    Match,
    Case,
    Then,
    Else,
    While,
    For,
    In,
    Do,
    Type,
    Enum,
    Struct,
    Import,
    End,
    Spawn,
    Self_,
    Ident(String),
    StringLit(String),
    RawStringLit(String),
    Number(f64),
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Colon,
    Dot,
    DotDot,
    DotDotEq,
    Comma,
    Pipe,
    PipeForward,
    Plus,
    PlusEq,
    Minus,
    MinusEq,
    Star,
    StarEq,
    Slash,
    SlashEq,
    Equals,
    FatArrow,
    Ge,
    Le,
    Percent,
    PercentEq,
    Eq,
    Ne,
    Gt,
    Lt,
    And,
    Or,
    Not,
}

#[derive(Debug, PartialEq, Clone)]
pub struct SpannedToken {
    pub kind: Token,
    pub line: usize,
}
